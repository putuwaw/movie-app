    public void view(Film film, String time, Integer ticket, ArrayList<String> chair, ArrayList<SnackDetail> snackDetail, Integer start, String idSnack){
        // init
        this.film = film;
        this.time = time;
        this.ticket = ticket;
        this.chair = chair;
        this.snackDetail = snackDetail;
        this.start = start;
        this.idSnack = idSnack;
        
        jButton1.setIcon(new javax.swing.ImageIcon(getClass().getResource("/" + this.idSnack + ".jpg")));
        
        jButton2.setText("S @" + AppUtil.getSnackDetailRepository().getPriceByIdSize(idSnack, "S"));
        jButton3.setText("M @" + AppUtil.getSnackDetailRepository().getPriceByIdSize(idSnack, "M"));
        jButton4.setText("L @" + AppUtil.getSnackDetailRepository().getPriceByIdSize(idSnack, "L"));

        buttonSize = new ArrayList<>();
        buttonSize.add(jButton2);
        buttonSize.add(jButton3);
        buttonSize.add(jButton4);
        
        jLabel6.setText(AppUtil.getSnackRepository().getNameById(idSnack));
        
        this.buySnack = 1;
        
        setLocationRelativeTo(null);
        setVisible(true);
    }

