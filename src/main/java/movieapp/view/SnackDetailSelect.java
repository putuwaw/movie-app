    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        boolean isNotValid = true;
        for (JButton button : buttonSize){
            if (button.getBackground() == Color.green){
                isNotValid = false;
            }
        }
        if (isNotValid){
            jLabel3.setText("Error! Silakan pilih size terlebih dahulu");
            jDialog1.pack();
            jDialog1.setLocationRelativeTo(null);
            jDialog1.setVisible(true);
        }else if (buySnack > availSnack){
            jLabel3.setText("Error! jumlah snack melebihi batas!");
            jDialog1.pack();
            jDialog1.setLocationRelativeTo(null);
            jDialog1.setVisible(true);
        }else{
            // check if exist or not
            boolean isExist = false;
            for (SnackDetail items : this.snackDetail){
                if (items.getId().equals(this.idSnack) && items.getSize().equals(this.tempSize)){
                    isExist = true;
                }
            }
            if (!isExist){
                // if not exist
                SnackDetail sd = new SnackDetail();
                sd.setId(this.idSnack);
                sd.setPrice(AppUtil.getSnackDetailRepository().getPriceByIdSize(idSnack, tempSize));
                sd.setSize(tempSize);
                sd.setStock(buySnack);
                this.snackDetail.add(sd);
            }else{
                for (SnackDetail items : this.snackDetail){
                    if (items.getId().equals(this.idSnack) && items.getSize().equals(this.tempSize)){
                        items.setStock(buySnack);
                    }
                }
            }
            // back to order confirmation
            OrderConfirmation oc = new OrderConfirmation();
            oc.view(this.film, this.time, this.ticket, this.chair, this.snackDetail);
            dispose();
        }
    }//GEN-LAST:event_jButton9ActionPerformed

